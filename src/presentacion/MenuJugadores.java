    private void botonIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIniciarActionPerformed

        if ((seleccion1Jugador.isSelected() && (colorJug1.getSelectedItem() == "seleccione un color 1"))
                || (seleccion2Jugadores.isSelected() && (colorJug1.getSelectedItem() == "seleccione un color 1" || colorJug2.getSelectedItem() == "seleccione un color 2"))
                || (seleccion3Jugadores.isSelected() && (colorJug1.getSelectedItem() == "seleccione un color 1" || colorJug2.getSelectedItem() == "seleccione un color 2" || colorJug3.getSelectedItem() == "seleccione un color 3"))
                || (seleccion4Jugadores.isSelected() && (colorJug1.getSelectedItem() == "seleccione un color 1" || colorJug2.getSelectedItem() == "seleccione un color 2" || colorJug3.getSelectedItem() == "seleccione un color 3" || colorJug4.getSelectedItem() == "seleccione un color 4"))) {
            JOptionPane.showMessageDialog(null, "Hay jugadores que aun no seleccionan color");

        } else if (colorJug1.getSelectedItem() == colorJug2.getSelectedItem()
                || colorJug1.getSelectedItem() == colorJug3.getSelectedItem()
                || colorJug1.getSelectedItem() == colorJug4.getSelectedItem()
                || colorJug2.getSelectedItem() == colorJug3.getSelectedItem()
                || colorJug2.getSelectedItem() == colorJug3.getSelectedItem()
                || colorJug3.getSelectedItem() == colorJug4.getSelectedItem()) {

            JOptionPane.showMessageDialog(null, "Todos los Jugadores deben tener colores Distintos");

        } else if (!(seleccionTablero8x8.isSelected() || seleccionTablero10x10.isSelected() || seleccionTablero12x12.isSelected())) {
            JOptionPane.showMessageDialog(null, "Se debe elegir un tamaño para el tablero");
        } else if (!(seleccion1Jugador.isSelected() || seleccion2Jugadores.isSelected() || seleccion3Jugadores.isSelected() || seleccion4Jugadores.isSelected())) {
            JOptionPane.showMessageDialog(null, "Se debe elegir un numero de jugadores");
        } else {
            UIGrafica gui = new UIGrafica(this.elegirTipo(), this.elegirJugadores());
            gui.setVisible(true);
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "Turno del jugador 1: " + nombreJug1.getText());
        }

    }//GEN-LAST:event_botonIniciarActionPerformed

