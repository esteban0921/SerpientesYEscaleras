    public ArrayList<Jugador> elegirJugadores() {
        int posicionJugador[] = {0, 0};
        ArrayList<Jugador> jugadores = new ArrayList<Jugador>();
        Jugador jugador1 = new Jugador(1, nombreJug1.getText(), new Ficha(colorJug1.getSelectedItem().toString()));
        Jugador jugador2 = new Jugador(2, nombreJug2.getText(), new Ficha(colorJug2.getSelectedItem().toString()));
        Jugador jugador3 = new Jugador(3, nombreJug3.getText(), new Ficha(colorJug3.getSelectedItem().toString()));
        Jugador jugador4 = new Jugador(4, nombreJug4.getText(), new Ficha(colorJug4.getSelectedItem().toString()));

        switch (tipo.getTamanoTablero()) {
            case 64:
                posicionJugador[0] = 7;
                break;
            case 100:
                posicionJugador[0] = 9;
                break;
            case 144:
                posicionJugador[0] = 11;
                break;
        }
        posicionJugador[1] = -1;

        if (seleccion1Jugador.isSelected()) {
            jugadores.add(0, jugador1);
            Jugador Fantasma1 = crearFantasma(2, tipo.getTamanoTablero());
            Jugador Fantasma2 = crearFantasma(3, tipo.getTamanoTablero());
            Jugador Fantasma3 = crearFantasma(4, tipo.getTamanoTablero());
            jugadores.add(1, Fantasma1);
            jugadores.add(2, Fantasma2);
            jugadores.add(3, Fantasma3);

            jugadores.get(0).setPosicion(posicionJugador);
        }
        if (seleccion2Jugadores.isSelected()) {
            jugadores.add(0, jugador1);
            jugadores.add(1, jugador2);
            for (int i = 0; i < jugadores.size(); i++) {
                jugadores.get(i).setPosicion(posicionJugador);
            }
        }
        if (seleccion3Jugadores.isSelected()) {
            jugadores.add(0, jugador1);
            jugadores.add(1, jugador2);
            jugadores.add(2, jugador3);
            for (int i = 0; i < jugadores.size(); i++) {
                jugadores.get(i).setPosicion(posicionJugador);
            }
        }
        if (seleccion4Jugadores.isSelected()) {
            jugadores.add(0, jugador1);
            jugadores.add(1, jugador2);
            jugadores.add(2, jugador3);
            jugadores.add(3, jugador4);
            for (int i = 0; i < jugadores.size(); i++) {
                jugadores.get(i).setPosicion(posicionJugador);
            }
        }
        return jugadores;//posiblemente nunca pase porque debe seleccionarse alguno para poder llamar este metodo
    }

